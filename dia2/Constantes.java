// Declaração da classe pública chamada Constantes
public class Constantes {

    // Método principal, onde a execução do programa começa
    public static void main(String[] args) {

        // Declara uma variável do tipo double chamada taxaJuros e a inicializa com o valor 11.92
        double taxaJuros = 11.92;

        // Exibe o valor atual da variável taxaJuros no console
        System.out.println(taxaJuros);

        // A linha abaixo está comentada, mas se fosse descomentada, alteraria o valor de taxaJuros
        // taxaJuros = 11.52;

        // Exibe o valor atualizado da variável taxaJuros no console
        // Como a linha anterior está comentada, o valor exibido será o mesmo que o valor inicial
        System.out.println(taxaJuros);
    }
}
