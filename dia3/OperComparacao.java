public class OperComparacao {

    public static void main(String[] args) {
        int a = 4;  // Declara uma variável 'a' e a inicializa com o valor 4
        int b = 5;  // Declara uma variável 'b' e a inicializa com o valor 5

        // Compara os valores das variáveis 'a' e 'b' e imprime o resultado
        // O operador '==' verifica se 'a' é igual a 'b'
        // Como 4 não é igual a 5, a expressão 'a == b' é falsa, então 'false' será impresso
        System.out.println(a == b); // Saída: false

        // Lista com operadores de comparação e suas funções:

        // ==  : Verifica se dois valores são iguais.
        // Exemplo: (4 == 5) é falso, pois 4 não é igual a 5.

        // !=  : Verifica se dois valores são diferentes.
        // Exemplo: (4 != 5) é verdadeiro, pois 4 é diferente de 5.

        // >   : Verifica se o valor à esquerda é maior que o valor à direita.
        // Exemplo: (4 > 5) é falso, pois 4 não é maior que 5.

        // <   : Verifica se o valor à esquerda é menor que o valor à direita.
        // Exemplo: (4 < 5) é verdadeiro, pois 4 é menor que 5.

        // >=  : Verifica se o valor à esquerda é maior ou igual ao valor à direita.
        // Exemplo: (4 >= 5) é falso, pois 4 não é maior ou igual a 5.

        // <=  : Verifica se o valor à esquerda é menor ou igual ao valor à direita.
        // Exemplo: (4 <= 5) é verdadeiro, pois 4 é menor ou igual a 5.
    }
}
